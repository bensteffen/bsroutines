    % Date: 2012-09-05 17:52:48
    % Packaged: 2017-04-27 17:58:04
char_flag = 0;
if ischar(path)
    path = {path};
    char_flag = 1;
end

if iscellstr(path)
    newPath = cell(size(path));
    for i = 1:numel(path)
        [p n e] = fileparts(path{i});
        newPath{i} = fullfile(p,[prefix n e]);
    end
end

if char_flag
    newPath = newPath{1};
end