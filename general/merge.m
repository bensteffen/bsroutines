if ischar(varargin{nargin})
    type = varargin{nargin};
    if ~(strcmp(type,'column_wise') || strcmp(type,'row_wise'))
        type = 'column_wise';
    end
    varargin = varargin(1:nargin - 1);
else
    type = 'column_wise';
end

switch type
    case 'column_wise'
        d = 2;
    case 'row_wise'
        d = 1;
end

C = cat(d,varargin{:});