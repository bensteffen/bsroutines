    %  IS WITH YOU. SHOULD THE PROGRAM PROVE DEFECTIVE, YOU ASSUME THE COST OF ALL NECESSARY
    %  SERVICING, REPAIR OR CORRECTION.
    %  
    % Author: Florian Haeussinger (florian.haeussinger@med.uni-tuebingen.de)
    % Date: 2016-04-21 14:55:48
    % Packaged: 2017-04-27 17:58:15
if nargin < 2
    dim = 1;
end

if dim == 2
    X = X';
end

[principal_components,eigen_values] = eig(cov(X));

principal_components = fliplr(principal_components);

eigen_values = eigen_values(diagIndices(size(eigen_values,1)));
eigen_values = flipud(eigen_values);



switch dim
    case 1
        principal_components = principal_components';
    case 2
        eigen_values = eigen_values';
end